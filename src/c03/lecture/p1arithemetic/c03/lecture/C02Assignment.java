package c03.lecture.p1arithemetic.c03.lecture;

public class C02Assignment {
	public static void main(String[] args) {
		// +=, -=, *=, /=, %=
		int a = 10;
		a = a + 5;
		System.out.println("a = " + a);

		int b = 10;
		b += 5;
		System.out.println("b = " + b);
		a = a - 3;
		a -= 3;
		System.out.println("a = " + a);
	}
}
